public class Primitives {
    public static void main(String[] args) {
        // primitives
        int rollno = 64;
        char letter = 'r'; // must be single quote
        float marks = 95.23f; // f must be in float.
        boolean check = false;
        double largeDecimal = 4567654.4567;
        long lagerInteger = 343434342432L;

        // non primitives - String , Array , Class

        // representation
        int a = 10;
        /* a is identifier and 10 is a literal */

        // writing millions(instead of using commas use underscore)
        System.out.println(234_000_000);
    }
}
